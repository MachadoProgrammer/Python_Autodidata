# Tuplas são coleções de dados imutáveis, ou seja, não podem ser alteradas.
# Não é possível adicionar, remover ou alterar elementos de uma tupla.

# Criando uma tupla
tupla = (1, 2, 3, 4, 5)
print(tupla)

# Acessando elementos de uma tupla
print(tupla[0])

# Tentando alterar um elemento de uma tupla
tupla[0] = 10
print(tupla)